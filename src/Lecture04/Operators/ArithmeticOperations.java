package Lecture04.Operators;

public class ArithmeticOperations {
    public static void main(String args[]) {
        int num1 = 5;
        int num2 = 6;
        // Unary operators: which require one operand
        System.out.println("num1 + num2: " + (num1 + num2));
        System.out.println("num1 - num2: " + (num1 - num2));
        System.out.println("num1 * num2: " + (num1 * num2));
        System.out.println("num1 / num2: " + (num1 / num2));
        System.out.println("num1 % num2: " + (num1 % num2));
    }
}
